/** Arama sorgusuna göre NACE kodlarını filtreler (kod veya açıklama). */
export function naceAra(sorgu: string): NaceKod[] {
  const q = sorgu.toLowerCase().replace(/\s+/g, " ").trim();
  if (!q) return NACE_KODLARI;
  return NACE_KODLARI.filter(
    (n) => n.kod.includes(q) || n.aciklama.toLowerCase().includes(q)
  );
}
